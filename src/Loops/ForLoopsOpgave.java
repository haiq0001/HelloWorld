package Loops;

public class ForLoopsOpgave {
    public static void main(String[] args) {

        System.out.println("1.");
        for (int i = 1; i <= 4; i++) {
            System.out.println("2 gange " + i + " = " + (2 * i));


        }

        System.out.println("2.");
        for (int i = 2; i < 13; i += 2) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i = 4; i < 80; i += 15) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i = 30; i > -21; i -= 10) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i = -7; i < 14; i += 4) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i = 97; i > 81; i -= 3) {
            System.out.print(i + " ");
        }
        System.out.println();

    }
}











